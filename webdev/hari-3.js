// Comparison Operators adalah membandingkan dua nilai
// sama dengan ditulis == , tisak sama dengan ditulis !==, 
// lebih besar dari ditulis > , lebih kecil dari ditulis <
// lebih besar dari atau sama dengan ditulis >= , lebih kecil dari atau sama dengan ditulis <=

if (1 == 3) {
  console.log("Sama");
} else {
  console.log("Tidak Sama");
}

if (10 > 5) {
    console.log("Lebih Besar"); 
} else {
    console.log("Tidak Lebih Besar");
}

